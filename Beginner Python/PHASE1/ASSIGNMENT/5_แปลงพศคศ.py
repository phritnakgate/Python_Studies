#แปลงค.ศ พ.ศ
work=int(input("เลือกการทำงาน => 1.แปลง ค.ศ เป็น พ.ศ. \n"+"2.แปลง พ.ศ เป็น ค.ศ.: "))
if work==1:
    C=int(input("ใส่ค.ศ:"))
    BE=C+543
    print("ค.ศ.", C, "= พ.ศ.", BE)
elif work==2:
    BE=int(input("ใส่พ.ศ:"))
    C=BE-543
    print("พ.ศ.", BE, "= ค.ศ.", C)
else:
    print("กรุณาใส่ค่าที่ถูกต้อง")
print("จบการทำงาน")