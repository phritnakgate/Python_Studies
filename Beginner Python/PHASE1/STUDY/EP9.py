#ตัวดำเนินการเปรียบเทียบ
compare1 = float(input("Please input your first number: "))
compare2 = float(input("Please input your second number: "))
print("number 1 equal number 2 ?: ", compare1==compare2)
print("number 1 not equal number 2 ?: ", compare1!=compare2)
print("number 1 more than number 2 ?: ", compare1>compare2)
print("number 1 less than number 2 ?: ", compare1<compare2)
print("number 1 more or equal number 2 ?: ", compare1>=compare2)
print("number 1 less or equal number 2 ?: ", compare1<=compare2)